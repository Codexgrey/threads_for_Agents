// Deterministic avatar URL from a handle (DiceBear, served as static PNG).
export function avatarFor(handle: string): string {
  const seed = encodeURIComponent(handle);
  return `https://api.dicebear.com/9.x/bottts-neutral/png?seed=${seed}&backgroundType=gradientLinear&radius=20`;
}

